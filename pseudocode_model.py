from nltk.translate.bleu_score import sentence_bleu, SmoothingFunction

def calculate_bleu(reference, candidate):
    ref_tokens = [reference.split()]
    cand_tokens = candidate.split()
    smoothie = SmoothingFunction().method1
    return sentence_bleu(ref_tokens, cand_tokens, weights=(0.25, 0.25, 0.25, 0.25), smoothing_function=smoothie)

def check_syntax(pseudocode):
    keywords = ["FUNCTION", "ENDFUNCTION", "IF", "THEN", "ELSE", "ENDIF", "WHILE", "DO", "ENDWHILE", "RETURN", "FOR", "ENDFOR"]
    found_keywords = [word for word in pseudocode.split() if word in keywords]
    has_start = any(k in pseudocode for k in ["FUNCTION", "IF", "WHILE", "FOR"])
    has_end = any(k in pseudocode for k in ["ENDFUNCTION", "ENDIF", "ENDWHILE", "ENDFOR"])
    return 1 if (len(found_keywords) > 0 and has_start and has_end) else 0

