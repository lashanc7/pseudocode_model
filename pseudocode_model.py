def calculate_accuracy(dataset, num_samples=20):
    correct_syntax_count = 0
    samples = dataset.select(range(min(num_samples, len(dataset))))

    for sample in samples:
        inputs = tokenizer([prompt_style.format(sample['instruction'], "")], return_tensors="pt").to("cuda")
        outputs = model.generate(**inputs, max_new_tokens=128, use_cache=True)
        generated = tokenizer.batch_decode(outputs, skip_special_tokens=True)[0]

        if any(word in generated for word in ["FUNCTION", "IF", "WHILE", "RETURN", "ENDIF"]):
            correct_syntax_count += 1

    return (correct_syntax_count / num_samples) * 100

